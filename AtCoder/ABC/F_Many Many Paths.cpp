ll const MOD = 1000000007;

// 前計算あり
const int MAX = 2000010;
ll fac[MAX], finv[MAX], inv[MAX];

// テーブルを作る前処理
void COMinit() {
    fac[0] = fac[1] = 1;
    finv[0] = finv[1] = 1;
    inv[1] = 1;
    for (ll i = 2; i < MAX; i++) {
        fac[i] = fac[i - 1] * i % MOD;
        inv[i] = MOD - inv[MOD % i] * (MOD / i) % MOD;
        finv[i] = finv[i - 1] * inv[i] % MOD;
    }
}

// 二項係数計算
ll COM(int n, int k) {
    if (n < k) return 0;
    if (n < 0 || k < 0) return 0;
    return fac[n] * (finv[k] * finv[n - k] % MOD) % MOD;
}

ll r1, c1, r2, c2;

int main() {
    cin >> r1 >> c1 >> r2 >> c2;

    COMinit();

    ll ans = 0;
    for (ll i = r1 + 1; i <= r2 + 1; i++) {
        ans += COM(i + c2, i);
        ans %= MOD;
        ans -= COM(i + c1 - 1, i);
        ans %= MOD;
    }
    if (ans < 0) ans += MOD;

    cout << ans << endl;

    return 0;
}